print("Counting up from 1 to 50:")
for num1 in range(1, 51):
    print(num1)
    

    
print("Counting down from 50 to 1:")
for num2 in range(51, -1):
    print(num2)
    

print("Counting up from 30 to 50:")
for num3 in range(30, 51):
    print(num3)
    

print("Counting down from 50 to 2:")
for num4 in range(50, 0, -2):
    print(num4)


print("Counting from 100 to 200:")
for num5 in range(100, 205, 5):
    print(num5)


